    vector<int> ans;
    stack<TreeNode*> st;
    while (root || !st.empty()) {
        while (root) {
            st.push(root);
            root = root->left;
        }
        root = st.top();
        ans.push_back(root->val);
        st.pop();
        root = root->right;
    }
